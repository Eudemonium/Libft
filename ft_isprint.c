#include "libft.h"

int	ft_isprint(int c)
{
	return ((c >= ' ' && c <= '\'') || \
			(c >= '(' && c <= '/') || \
			(c >= '0' && c <= '?') || \
			(c >= '@' && c <= '_') || \
			(c >= '`' && c <= '~'));
}
